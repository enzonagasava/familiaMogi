<script setup lang="ts">
import { ref, reactive } from 'vue'
import AuthLayout from '@/layouts/AuthLayout.vue'
import { Head, usePage } from '@inertiajs/vue3'
import { Inertia } from '@inertiajs/inertia'


const page = usePage();

const rawProduct = page.props.products || {};

const products = reactive({
  ...rawProduct,
  tamanhos: rawProduct.tamanhos ? JSON.parse(rawProduct.tamanhos) : [],
});
console.log(products.id);

const previewImagens = ref<string[]>([])

// Para controlar a imagem que está em modo de visualização maior
const imagemModal = ref<string | null>(null)

function adicionarTamanho() {
  products.tamanhos.push({ nome: '', preco: 0 })
}

function removerTamanho(index: number) {
  products.tamanhos.splice(index, 1)
}
const imagensFiles = ref<File[]>([]);
const fileInput = ref<HTMLInputElement | null>(null);

function onFilesChange(event: Event) {
  const target = event.target as HTMLInputElement;
  if (target.files) {
    const selectedFiles = Array.from(target.files);

    // Acumula os arquivos já selecionados + novos, evitando duplicados (nome + tamanho)
    const allFiles = [...imagensFiles.value];
    for (const file of selectedFiles) {
      if (!allFiles.some(f => f.name === file.name && f.size === file.size)) {
        allFiles.push(file);
      }
    }
    imagensFiles.value = allFiles;

    // Atualiza os previews
    previewImagens.value = imagensFiles.value.map(file => URL.createObjectURL(file));

    // Limpa o input para permitir selecionar os mesmos arquivos novamente
    if (fileInput.value) {
      fileInput.value.value = '';
    }
  } else {
    imagensFiles.value = [];
    previewImagens.value = [];
  }
}
function removerImagem(index: number) {
  // Revoga a URL para liberar memória
  URL.revokeObjectURL(previewImagens.value[index])

  // Remove dos arrays
  previewImagens.value.splice(index, 1)
  imagensFiles.value.splice(index, 1)
}

// Abrir modal com imagem maior
function abrirModal(index: number) {
  imagemModal.value = previewImagens.value[index]
}

// Fechar modal
function fecharModal() {
  imagemModal.value = null
}


function handleSubmit() {
  const formData = new FormData()
  formData.append('_method', 'PUT');
  formData.append('nome', products.nome)
  formData.append('descricao', products.descricao)
  formData.append('estoque', products.estoque.toString())
  formData.append('tamanhos', JSON.stringify(products.tamanhos))
  imagensFiles.value.forEach((file, index) => {
  formData.append('imagens[]', file)
  })
  Inertia.post(`/produtos/update-produto/${products.id}`, formData, {
      forceFormData: true,
      onSuccess: () => {
        alert('Produto salvo com sucesso!')
        // Resetar formulário
        products.nome = ''
        products.descricao = ''
        products.tamanhos = [{ nome: '200g', preco: 19.90 }, { nome: '1kg', preco: 79.90 }]
        products.estoque = 0
        products.imagemFile = null
      },
      onError: (errors) => {
        alert('Erro ao salvar produto')
        console.error(errors)
      }
  })
}
</script>

<template>
  <Head>
    <title>Adicionar Produto - Família Mogi</title>
    <meta name="description" content="Gerenciamento de produtos da Família Mogi" />
  </Head>

  <AuthLayout>
    <div class="flex min-h-screen bg-gray-100">
      <!-- Main Content -->
      <main class="flex-1 p-10">
        <div class="max-w-4xl mx-auto bg-white rounded shadow p-8">
          <h2 class="text-2xl font-bold mb-6">Adicionar Novo Produto</h2>

          <form @submit.prevent="handleSubmit" class="flex flex-col gap-6">
            <div>
              <label for="nome" class="block font-semibold mb-2 text-gray-700">Nome do Produto</label>
              <input
                id="nome"
                v-model="products.nome"
                type="text"
                placeholder="Digite o nome do produto"
                class="w-full rounded border border-gray-300 px-4 py-2 focus:outline-none focus:ring-2 focus:ring-green-500"
                required
              />
            </div>

            <div>
              <label for="descricao" class="block font-semibold mb-2 text-gray-700">Descrição do Produto</label>
              <textarea
                id="descricao"
                v-model="products.descricao"
                rows="4"
                placeholder="Digite a descrição do produto"
                class="w-full rounded border border-gray-300 px-4 py-2 focus:outline-none focus:ring-2 focus:ring-green-500"
                required
              ></textarea>
            </div>

            <div>
              <label class="block font-semibold mb-2 text-gray-700">Tamanhos e Preços</label>
              <div v-for="(tamanho, index) in products.tamanhos" :key="index" class="flex gap-4 items-center mb-3">
                <input
                  v-model="tamanho.nome"
                  type="text"
                  placeholder="Ex: 200g, 1kg"
                  class="flex-1 rounded border border-gray-300 px-4 py-2 focus:outline-none focus:ring-2 focus:ring-green-500"
                  required
                />
                <input
                  v-model.number="tamanho.preco"
                  type="number"
                  min="0"
                  step="0.01"
                  placeholder="Preço"
                  class="w-32 rounded border border-gray-300 px-4 py-2 focus:outline-none focus:ring-2 focus:ring-green-500"
                  required
                />
                <button
                  type="button"
                  @click="removerTamanho(index)"
                  class="text-red-600 hover:text-red-800 font-bold px-2 py-1 rounded border border-red-600"
                  title="Remover tamanho"
                >
                  &times;
                </button>
              </div>
              <button
                type="button"
                @click="adicionarTamanho"
                class="mt-2 bg-green-600 hover:bg-green-700 text-white font-semibold px-4 py-2 rounded transition"
              >
                + Adicionar Tamanho
              </button>
            </div>

            <div>
              <label for="estoque" class="block font-semibold mb-2 text-gray-700">Quantidade em Estoque</label>
              <input
                id="estoque"
                v-model.number="products.estoque"
                type="number"
                min="0"
                placeholder="Quantidade disponível"
                class="w-full rounded border border-gray-300 px-4 py-2 focus:outline-none focus:ring-2 focus:ring-green-500"
                required
              />
            </div>

            <div>
            <label class="block font-semibold mb-2 text-gray-700">Imagem do Produto</label>
                <input
                ref="fileInput"
                type="file"
                multiple
                accept="image/*"
                @change="onFilesChange"
                class="block w-full text-sm text-gray-600 file:mr-4 file:py-2 file:px-4 file:rounded file:border-0 file:text-sm file:bg-green-600 file:text-white hover:file:bg-green-700 cursor-pointer"
                />                
                <p class="mt-1 text-gray-600">
                {{ imagensFiles.length }} arquivo{{ imagensFiles.length !== 1 ? 's' : '' }} selecionado{{ imagensFiles.length !== 1 ? 's' : '' }}.
                </p>                
                <div v-if="previewImagens.length" class="mt-4 flex gap-4 flex-wrap">
            <div v-if="previewImagens.length" class="mt-4 flex gap-4 flex-wrap">
            <div
                v-for="(products, index) in previewImagens"
                :key="index"
                class="relative w-24 h-24 rounded overflow-hidden border border-gray-300 shadow cursor-pointer"
            >
                <img
                :src="products.imagem_path ? product.imagem_path : ''"
                alt="Preview da Imagem"
                class="w-full h-full object-cover"
                @click="abrirModal(index)"
                />
                <a
                @click.stop="removerImagem(index)"
                class="absolute top-1 right-1 bg-red-600 text-white rounded-full w-5 h-5 flex items-center justify-center text-xs hover:bg-red-800"
                title="Remover imagem"
                >
                ×
                </a>
            </div>
            </div>
            </div>
            </div>

            <div class="flex justify-end">
              <button
                type="submit"
                class="bg-green-600 hover:bg-green-700 text-white font-semibold px-6 py-2 rounded transition"
              >
                Salvar Produto
              </button>
            </div>
          </form>
        </div>
      </main>
    </div>

    <!-- Modal para imagem maior -->
    <div
      v-if="imagemModal"
      @click.self="fecharModal"
      class="fixed inset-0 bg-black bg-opacity-70 flex items-center justify-center z-50"
    >
      <div class="relative max-w-3xl max-h-[80vh]">
        <button
          @click="fecharModal"
          class="absolute top-2 right-2 text-white bg-black bg-opacity-50 rounded-full w-8 h-8 flex items-center justify-center text-xl font-bold hover:bg-opacity-80"
          title="Fechar"
        >
          ×
        </button>
        <img
          :src="imagemModal"
          alt="Imagem ampliada"
          class="max-w-full max-h-[80vh] rounded"
        />
      </div>
    </div>
  </AuthLayout>
</template>

